    private void tblproductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblproductMouseClicked
      int row = tblproduct.getSelectedRow();
      String selectId = tblproduct.getValueAt(row, 0).toString();
      String sql="SELECT * FROM users WHERE id='"+selectId+"' ";
        try {
            pst=con.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
               //combo.setText(rs.getString("id")); 
               txtUsername1.setText(rs.getString("username"));
               txtPassword.setText(rs.getString("password"));
               txtFname.setText(rs.getString("fname"));
               txtLname.setText(rs.getString("lname"));
               txtTel.setText(rs.getString("tel"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Form1.class.getName()).log(Level.SEVERE, null, ex);
        }
      System.out.println(selectId);
    }//GEN-LAST:event_tblproductMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       try{
           String sql="INSERT INTO users(username,password,fname,lname,tel) VALUES(?,?,?,?,?)";
           pst=con.prepareStatement(sql);
           
           pst.setString(1, combo.getSelectedItem().toString());
           pst.setString(2, txtPassword.getText());
           pst.setString(3, txtFname.getText());
           pst.setString(4, txtLname.getText());
           pst.setString(5, txtTel.getText());
           pst.execute();
           
           JOptionPane.showMessageDialog(this,"Save","Save Succss", JOptionPane.INFORMATION_MESSAGE);
           showDataToForm();
           Clear();
       }catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed


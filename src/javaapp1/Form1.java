    private void txtUsername1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsername1KeyReleased
       try {
            String sql="SELECT * FROM users";
            pst=con.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next()){
                String username = rs.getString("username");
                if(username.equalsIgnoreCase(txtUsername1.getText())){
                   JOptionPane.showMessageDialog(this, "รหัสซ้ำกัน", "แจ้งเติอน",JOptionPane.WARNING_MESSAGE);
                    Clear();
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Form1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtUsername1KeyReleased

